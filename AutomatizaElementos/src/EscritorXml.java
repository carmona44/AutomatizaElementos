	public void generaXml(String bloqueEspecificos[]) {

		String tareas = (this.persona.equals("Jurídica")) ? this.TAREASJURIDICA : this.TAREASFISICA;
		this.archivos = new String[][]{{"configuracion.xml", this.CONFIGURACION}, {"correo.xml", this.correo}, {"tareas.xml", tareas}, {"exportacion.xml", this.EXPORTACION}};
		
		try {
			FileWriter fw = new FileWriter("elementos.xml");
			BufferedWriter bw = new BufferedWriter(fw);
			
			bw.write(escribeElementos(bloqueEspecificos));
			bw.flush();
			
			for(int i=0; i<archivos.length; i++) {
				fw = new FileWriter(archivos[i][0]);
				bw = new BufferedWriter(fw);
				bw.write(archivos[i][1]);
				bw.flush();
			}
			
			JOptionPane.showMessageDialog(capa, "Se han creado los ficheros.", "Creación de XMLs", JOptionPane.INFORMATION_MESSAGE);
			
			bw.close();
			
		} catch (IOException e) {
			
			JOptionPane.showMessageDialog(capa, e, "ERROR", JOptionPane.ERROR_MESSAGE);
		}
		
	}
	
